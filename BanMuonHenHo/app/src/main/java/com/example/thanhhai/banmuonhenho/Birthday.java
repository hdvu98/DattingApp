package com.example.thanhhai.banmuonhenho;

public class Birthday {
    Integer day;
    Integer month;
    Integer year;


    public Birthday() {
        this.day = 1;
        this.month = 1;
        this.year = 2000;
    }

    public Birthday(Integer day, Integer month, Integer year) {
        this.day = day;
        this.month = month;
        this.year = year;
    }
}
